    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
            int selectedIndex = jList1.getSelectedIndex();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this, "Please select a question to edit.");
            return;
        }
        
        Question selectedQuestion = questions.get(selectedIndex);
        AddQuestionDialog dialog = new AddQuestionDialog(this, true, selectedQuestion);
        dialog.setVisible(true);
        
        if (dialog.isSaved()) {
            questions.set(selectedIndex, dialog.getQuestion());
            updateQuestionList();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

