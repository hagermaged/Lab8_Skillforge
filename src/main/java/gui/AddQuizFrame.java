     private void updateQuestionList() {
        questionListModel.clear();
        for (int i = 0; i < questions.size(); i++) {
            Question q = questions.get(i);
            questionListModel.addElement((i + 1) + ". " + q.getQuestionText() + " (" + q.getPoints() + " points)");
        }
    }
