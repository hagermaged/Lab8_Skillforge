    //get certificate from its id
    public Certificate getCertificate(String certificateId) {
        try {
            CertificateService certService = new CertificateService();
            Certificate certificate = certService.getCertificateById(certificateId);
            return certificate;
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error loading certificate: " + ex.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            return null;
        }
    }

    private void loadCertificateData() {
        if (certificate != null) {
            // Set all the text fields with certificate data
            studentName.setText(certificate.getStudentName());
            courseName.setText(certificate.getCourseName());
            certificateIdBox.setText(certificate.getCertificateId());

            // Format the date nicely
            java.text.SimpleDateFormat dateFormat = new java.text.SimpleDateFormat("MMMM dd, yyyy");
            String formattedDate = dateFormat.format(certificate.getIssueDate());
            issuedDate.setText(formattedDate);

            // Make all fields read-only since it's a certificate display
            studentName.setEditable(false);
            courseName.setEditable(false);
            certificateIdBox.setEditable(false);
            issuedDate.setEditable(false);

        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Certificate not found!",
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }

