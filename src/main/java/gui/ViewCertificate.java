    private void downloadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_downloadButtonActionPerformed
        String[] options = {"Download as Image", "Download as JSON", "Cancel"};
        int choice = javax.swing.JOptionPane.showOptionDialog(this,
                "Choose download format:",
                "Download Certificate",
                javax.swing.JOptionPane.DEFAULT_OPTION,
                javax.swing.JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[0]);

        if (choice == 0) {
            downloadAsImage();
        } else if (choice == 1) {
            downloadAsJSON();
        }
    }//GEN-LAST:event_downloadButtonActionPerformed
    private void downloadAsImage() {
        try {
            // Create image from the CertificatePanel only
            java.awt.image.BufferedImage image = new java.awt.image.BufferedImage(
                    CertificatePanel.getWidth(),
                    CertificatePanel.getHeight(),
                    java.awt.image.BufferedImage.TYPE_INT_RGB
            );

            java.awt.Graphics2D g2d = image.createGraphics();
            CertificatePanel.printAll(g2d); // Renders the certificate panel to image
            g2d.dispose();

            // Save as PNG
            javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
            fileChooser.setSelectedFile(new java.io.File(
                    "Certificate_" + certificate.getCertificateId() + ".png"
            ));

            if (fileChooser.showSaveDialog(this) == javax.swing.JFileChooser.APPROVE_OPTION) {
                javax.imageio.ImageIO.write(image, "PNG", fileChooser.getSelectedFile());
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Certificate image saved successfully!",
                        "Download Complete",
                        javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error saving image: " + ex.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

    private void downloadAsJSON() {
        try {
            if (certificate == null) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "No certificate data to download!",
                        "Error",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Create JSON content
            String jsonContent = String.format(
                    "{\n"
                    + "  \"certificateId\": \"%s\",\n"
                    + "  \"studentName\": \"%s\",\n"
                    + "  \"courseName\": \"%s\",\n"
                    + "  \"issueDate\": \"%s\",\n"
                    + "  \"studentId\": \"%s\",\n"
                    + "  \"courseId\": \"%s\"\n"
                    + "}",
                    certificate.getCertificateId(),
                    certificate.getStudentName(),
                    certificate.getCourseName(),
                    new java.text.SimpleDateFormat("yyyy-MM-dd").format(certificate.getIssueDate()),
                    certificate.getStudentId(),
                    certificate.getCourseId()
            );

            // Show file chooser to save the JSON file
            javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
            fileChooser.setSelectedFile(new java.io.File(
                    "Certificate_" + certificate.getCertificateId() + ".json"
            ));

            int result = fileChooser.showSaveDialog(this);
            if (result == javax.swing.JFileChooser.APPROVE_OPTION) {
                java.io.File file = fileChooser.getSelectedFile();

                // Make sure it has .json extension
                if (!file.getName().toLowerCase().endsWith(".json")) {
                    file = new java.io.File(file.getAbsolutePath() + ".json");
                }

                // Write the JSON content to file
                java.io.FileWriter writer = new java.io.FileWriter(file);
                writer.write(jsonContent);
                writer.close();

                javax.swing.JOptionPane.showMessageDialog(this,
                        "Certificate downloaded successfully!\n"
                        + "Saved as: " + file.getName(),
                        "Download Complete",
                        javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error downloading certificate: " + ex.getMessage(),
                    "Download Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

