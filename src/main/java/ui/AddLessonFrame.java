    private void addLessonButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addLessonButtonActionPerformed

        String title = NewLessonTitle1.getText().trim();
        String content = newLessonContent.getText().trim();
        String resources = newLessonResources.getText().trim();
        if (courseId == null) {
            JOptionPane.showMessageDialog(this,
                    "Course ID is not set. Cannot add lesson.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        Course course = courseService.getCourseFromInstance(courseId);
        if (course == null) {
            JOptionPane.showMessageDialog(this,
                    "Course not found in memory.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        String id = idGenerator.generateLessonId(courseService.getAllCourses());

        // Validate inputs
        if (title.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Please enter a lesson title.",
                    "Input Error",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (content.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Please enter lesson content.",
                    "Input Error",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            boolean success = courseService.addLesson(courseId, id, title, content, resources);
            if (success) {
                JOptionPane.showMessageDialog(this,
                        "Lesson added successfully!",
                        "Success",
                        JOptionPane.INFORMATION_MESSAGE);

                if (parent != null) {
                    parent.loadLessonsToTable();
                }

                dispose();
                if (parent != null) {
                    parent.setVisible(true);
                }

            } else {
                JOptionPane.showMessageDialog(this,
                        "Failed to add lesson.",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error adding lesson: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_addLessonButtonActionPerformed

